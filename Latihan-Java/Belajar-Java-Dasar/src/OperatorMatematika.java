public class OperatorMatematika {
    public static void main(String[] args) {
        Integer a = 100;
        Integer b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a / b);
        System.out.println(a * b);
        System.out.println(a % b);

        Integer z = 100;

        z += 10;
        System.out.println(z);

        z -= 10;
        System.out.println(z);

        z *= 100;
        System.out.println(z);

//        *******************************

        Integer d = 1000;

        d++;
        System.out.println(d);

        d--;
        System.out.println(d);

        System.out.println(!true);
    }
}
