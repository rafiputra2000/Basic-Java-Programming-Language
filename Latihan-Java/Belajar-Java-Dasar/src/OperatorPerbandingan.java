public class OperatorPerbandingan {
    public static void main(String[] args) {
        Integer a = 100;
        Integer b = 50;

        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
    }
}
